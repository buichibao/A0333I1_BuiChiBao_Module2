package titv_youtube;

public class bai12_cac_toan_tu_mot_ngoi {
    public static void main(String[] args) {
        int a = 10;
        boolean b = false;
        System.out.println("+a = "+(+a));//10
        System.out.println("-a = "+(-a));//-10
        System.out.println("!b = "+(!b));//true

        System.out.println("a = "+a);//10
        System.out.println("++a = "+(++a));//11
        System.out.println("a++ = "+(a++));//11
        System.out.println("a = "+a);//12

        System.out.println("a = "+a);//12
        System.out.println("--a = "+(--a));//11
        System.out.println("a-- = "+(a--));//11
        System.out.println("a = "+a);//10



    }
}
